def ctrlCreater(ctrlname,curInfo,colorId=0):
	temCurShape = pm.createNode('nurbsCurve')
	temCurTra = temCurShape.getTransform().rename(ctrlname)
	pm.mel.eval(curInfo)
	temCurShape.overrideEnabled.set(1)
	temCurShape.overrideColor.set(colorId)
	temCurTra.scale.set(15,15,15)
	pm.makeIdentity(temCurTra,apply=True,s=True)
	return temCurTra